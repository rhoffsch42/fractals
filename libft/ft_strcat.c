#include "libft.h"

char		*ft_strcat(char *s1, const char *s2)
{
	int		i;
	int		j;

	j = ft_strlen(s1);
	i = 0;
	if (s1[i] == '\0')
	{
		s1[j] = s2[i];
		j++;
		i++;
	}
	while (s2[i])
	{
		s1[j] = s2[i];
		i++;
		j++;
	}
	s1[j] = '\0';
	return (s1);
}
